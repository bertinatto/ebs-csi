func (d *Driver) NodeGetId(ctx context.Context, req *csi.NodeGetIdRequest) (*csi.NodeGetIdResponse, error) {
	m := d.cloud.GetMetadata()
	return &csi.NodeGetIdResponse{
		NodeId: m.GetInstanceID(),
	}, nil
}

